//--------------------------------------------------------------
void ofApp::keyPressed(int key){
	switch (key) {
	case ' ':
		active = !active;
		break;
	case 'f':
		ofToggleFullscreen(); //フルスクリーン切り替え
		break;
	case 's':
		ofShowCursor(); //カーソル表示
		break;
	case 'h':
		ofHideCursor(); //カーソル非表示
		break;
	default:
		break;
	}
}

